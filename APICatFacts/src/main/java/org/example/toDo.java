package org.example;

public class toDo {
    private int userId;
    private int id;
    private String title;
    private boolean completed;
    public toDo(){

    }

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public boolean isCompleted() {
        return completed;
    }
}
